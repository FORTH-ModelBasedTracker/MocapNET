void convertStringToCFriendly(char * str)
{
  if (str==0) { return;}   
  while (*str!=0)
  {
    switch (*str)
    {
       case '-' :  
       case '.' :  
       case '_' :  
       *str='_';
       break;
       
       default : 
       *str=toupper(*str);
    };
    ++str;
  } 
    
}



